// Freeze the football team object to prevent modifications
Object.freeze(myFavoriteFootballTeam);

// Destructure properties from the football team object
const { sport, team, year, players } = myFavoriteFootballTeam;
const { coachName } = myFavoriteFootballTeam.headCoach;

// Set text content of HTML elements to corresponding data
typeOfSport.textContent = sport;
teamName.textContent = team;
worldCupYear.textContent = year;
headCoach.textContent = coachName;

// Function to render player cards
const setPlayerCards = (arr = players) => {
  // Clear existing player cards
  playerCards.innerHTML = "";

  // Iterate over player array and generate HTML for each player card
  arr.forEach(({ name, position, number, isCaptain, nickname }) => {
    const captainText = isCaptain ? "(Captain)" : ""; // Add "(Captain)" text if player is captain
    const nicknameText = nickname !== null ? nickname : "N/A"; // Display nickname or "N/A" if null
    const playerCardHTML = `
      <div class="player-card">
        <h2>${name} ${captainText}</h2>
        <p>Position: ${position}</p>
        <p>Number: ${number}</p>
        <p>Nickname: ${nicknameText}</p>
      </div>
    `;
    // Append player card HTML to playerCards element
    playerCards.innerHTML += playerCardHTML;
  });
};

// Event listener for dropdown list change
playersDropdownList.addEventListener("change", (e) => {
  // Clear player cards when dropdown selection changes
  playerCards.innerHTML = "";

  // Use a switch statement to determine which players to display based on dropdown selection
  switch (e.target.value) {
    case "nickname":
      // Filter players with a nickname and render player cards
      setPlayerCards(players.filter((player) => player.nickname !== null));
      break;
    // Filter players by position and render player cards
    case "forward":
      setPlayerCards(players.filter((player) => player.position === "forward"));
      break;
    case "midfielder":
      setPlayerCards(
        players.filter((player) => player.position === "midfielder")
      );
      break;
    case "defender":
      setPlayerCards(
        players.filter((player) => player.position === "defender")
      );
      break;
    case "goalkeeper":
      setPlayerCards(
        players.filter((player) => player.position === "goalkeeper")
      );
      break;
    // Render all players by default
    default:
      setPlayerCards();
  }
});
